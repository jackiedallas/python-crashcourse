# add tab to text using '\t'
print("Python")
print("\tPython")

# add newline in a string using '\n'
print("\nLanguages:\nPython\nC\nJavaScript")

# combine newline and tabs 
print("\nLanguages:\n\tPython\n\tC\n\tJavaScript")

# strip whitespace using '.strip()' method. you can also remove whitespace from left or right using .lstrip and .rstrip respectively.
favoriteLanguage = '   python '
favoriteLanguage = favoriteLanguage.strip()
print(favoriteLanguage)

