# put magicians in a list and loop through and print the names
magicians = ['alice', 'david', 'carolina', 'chris']
for magician in magicians:
    print(f"{magician.title()}, that was a great trick!") # print a message to each item in the list
    print(f"I can't wait to see your next trick, {magician.title()}.\n")
    
print(f"Thank you, everyone. That was a great magic show.\n")
